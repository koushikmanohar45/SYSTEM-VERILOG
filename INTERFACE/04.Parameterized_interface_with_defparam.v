
//DESIGN(VERILOG)
module down_counter #(parameter N=4)
  (
  input clk,
  input rst,
  output reg[N-1:0] count
  );
  
  always @(posedge clk) begin
    if(rst)
      count<={N{1'b1}};
    else begin
      if(count<=((2**N)-1))
         count<=count-1;
      else
        count<={N{1'b1}};
    end 
  end
  
  endmodule

//TESTBENCH

module down_counter_tb;
  logic clk=0;
  always #5 clk=~clk;
  
  //interface intialization
  counter itf(clk);
  defparam itf.N=5;
  
  
  //dut instance for verilog file
  down_counter  dut  (.clk(clk),.rst(itf.rst),.count(itf.count));
  defparam dut.N=5;
  
  initial begin
    $dumpfile("v.vcd");
    $dumpvars(0,down_counter_tb);
    $monitor("TIME=%0T ||CLK=%B RST=%B || COUNT=%0D",$time,clk,itf.rst,itf.count);
     clk=0;
    itf.rst=1;
    #20 itf.rst=0; 
    #400 itf.rst=1;
    #20 itf.rst=0;
    #200 $finish;
  end
endmodule

interface counter #(parameter N=4)(input logic clk);
  logic rst;
  logic [N-1:0]count;  
endinterface

