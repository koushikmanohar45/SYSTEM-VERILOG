class dynamic_array;
  rand logic [3:0] a[];
  rand logic [7:0] b[];
  
  constraint c1{a.size()==5;};
  constraint c2{b.size()==4;}
  
   task display();
     $display("a=%p ",a);
     $display("b=%p",b);
  endtask
  
endclass

module randomization();
  int i;
  dynamic_array d1;
  dynamic_array d2;
  
  initial begin
    d1=new();
    d2=new();
    $display("=========================================================");
    $display("              DYNAMIC ARRAY RANDOMIZATION                ");
    $display("=========================================================");
    
    repeat(5)begin
      $display("-------------------------d1.randomize()-------------------");
      d1.randomize();
      d1.display();
      $display("-----------------------------------------------------------");
    end
    
    repeat(5)begin
      $display("-------------------------d2.randomize()-------------------");
      d2.randomize();
      d2.display();
      $display("-----------------------------------------------------------");
    end
    
  end
endmodule

