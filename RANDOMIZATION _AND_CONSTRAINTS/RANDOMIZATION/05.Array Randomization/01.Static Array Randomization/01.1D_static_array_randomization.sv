class static_array;
  rand logic [7:0] a[5];
  rand logic [4:0] b[7];
  
  constraint con{ 
                foreach(a[i])
                 a[i]%2==0;
    
                foreach(b[i])
                  b[i]%2!=0;
               };
  
   task display();
     $display("a=%p b=%p",a,b);
  endtask
  
endclass

module randomization();
  int i;
  static_array s1;
  initial begin
    s1=new();
    for(i=10;i>=0;i--)begin
      $display("----------------------------------------ITERATION %0d---------------------------------------",i);
      s1.randomize();
      s1.display();
    end
  end
endmodule



