class queue;
  rand logic [3:0] a[$];
  rand logic [7:0] b[$];
  
  constraint c1{a.size()==5;
                foreach(a[i])
                  a[i] inside {[1:10]};
               }
  
  constraint c2{b.size()==8;
               foreach(b[i])
                 b[i] inside {[1:40]};
               }

  
   task display();
     $display("a=%p ",a);
     $display("b=%p",b);
  endtask
  
endclass

module randomization();
  int i;
  queue q1;
  queue q2;
  
  initial begin
    q1=new();
    q2=new();
    
    $display("=========================================================");
    $display("                    QUEUE RANDOMIZATION                ");
    $display("=========================================================");
    
    repeat(5)begin
      $display("-------------------------q1.randomize()-------------------");
      q1.randomize();
      q1.display();
      $display("-----------------------------------------------------------");
    end
    
    repeat(5)begin
      $display("-------------------------q2.randomize()-------------------");
      q2.randomize();
      q2.display();
      $display("-----------------------------------------------------------");
    end
    
  end
endmodule


