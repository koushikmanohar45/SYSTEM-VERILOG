class transaction;
  bit rst;
  rand bit [7:0] data;
  rand bit [3:0] w_addr;
  rand bit [3:0] r_addr;
  bit w_en,r_en;
  string operation;
  
  constraint c1{
                w_addr>=4;
                r_addr>=4;
               }

  function void generate_operation();

    randcase
      5 : begin 
          operation="READ_only";
          w_en=0;
          r_en=1;
          rst=0;
         end
      3 :begin
          operation="WRITE_only";
          w_en=1;
          r_en=0;
          rst=0;
         end
      4 :begin
          operation="WRITE & READ";
          w_en=1;
          r_en=1;
          rst=0;
         end
      2 :begin 
        operation="RESET";
        rst=1;
        end
    endcase

  endfunction

endclass



module test;

  transaction t;

  initial begin
    t=new();
    repeat(20) begin
      t.randomize();
      $display("----------------------------------------");
      $display("                RANDOMIZE               ");
      $display("----------------------------------------");
      $display("W_ADDR=%0d   R_ADDR=%0d  DATA=%0D \n",t.w_addr,t.r_addr,t.data);
      t.generate_operation();
      $display("----------------------------------------");
      $display("OPERATION = %s",t.operation);
      $display("----------------------------------------");
      $display("RST=%b  W_EN=%0d  R_en=%0d \n",t.rst,t.w_en,t.r_en);
      $display("===========================================================================");
    end
  end

endmodule


