class i_c;
  rand int a;
  rand bit [4:0]b;
  constraint  c1{a inside {2,5,[10:15],17,[21:30]};} 
  constraint c2{ !(b inside {2,5,[10:15],17,[21:30]});}
endclass

module inverted_inside_constraint();
  i_c s;
  initial begin
    s=new();
    $display("--------------inverted inside constraint------------------");
    repeat(20)begin
      s.randomize();
      $display("a=%0d b=%0d",s.a,s.b);
      $display("----------------------------------------------------------");
    end
  end
endmodule


