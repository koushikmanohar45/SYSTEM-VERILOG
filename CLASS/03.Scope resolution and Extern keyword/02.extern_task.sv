class extern_keyword;
  
  bit[7:0] data_in;
  bit[7:0] data_out;

  
  static bit [7:0] memory[0:15];
  
  function new(bit [7:0] data_in);
    this.data_in=data_in;
  endfunction
  
  //task declared as extern
  extern task write_memory(bit[3:0]addr);
  extern task read_memory(bit[3:0]addr);
   
endclass
    
    
//task outside class using extern and scopre resolution
task extern_keyword::write_memory(bit[3:0]addr);

  memory[addr]=data_in;
  
endtask
  
  
task extern_keyword::read_memory(bit[3:0]addr);

      data_out=memory[addr];
  
endtask
  
 
    
