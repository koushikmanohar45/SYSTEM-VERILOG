class extern_keyword;
  
  rand bit[2:0] data1;
  rand bit[2:0] data2;
  
  constraint c1 {data1!=data2;}
  
  //function declared as extern
  extern function void display;
   
endclass
    
    
//function outside class using extern and scopre resolution
function void extern_keyword::display();
   
  $display("checking for data1=3 and data2=2..............");

endfunction
 
    
module test();
  
  extern_keyword e;
  
  initial begin
    
    forever begin
      e=new();
      e.randomize();
      $display("----------------------------------------------------------------------");
      
      $display("[time=%0t] data1=%0d,data2=%0d",$time,e.data1,e.data2);
      e.display();
      
      if(e.data1==3 && e.data2==2)begin
        $display("PASS");
        $finish;
    end
      else begin
        $display("FAIL");
      end
      
      $display("----------------------------------------------------------------------");
      #1;
    end
  end
endmodule


